#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char	c;
	long	n;

	n = nb;
	if (nb < 0)
	{
		write (fd, "-", 1);
		n *= -1;
	}
	if (n >= 10)
		ft_putnbr_fd((n / 10), fd);
	c = (n % 10) + '0';
	write (fd, &c, 1);
}

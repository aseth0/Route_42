#include "libft.h"
#include <stdlib.h>
#include <stdio.h>
/**
 @brief Find an integger into a string
*/
int	ft_atoi(const char *str)
{
	int		nb;
	int		i;
	int		sign;

	nb = 0;
	i = 0;
	sign = 1;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = sign * -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	return (nb * sign);
}
